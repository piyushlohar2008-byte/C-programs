#include<iostream>
using namespace std;
int main(){
	cout<<"Capital A to Z ASCII value : "<<endl;
		cout<<endl;
	for(char ch='A';ch<='Z';ch++){
		cout<<ch<<" : "<<int(ch)<<endl;
	}
	cout<<endl;
	cout<<"small a to z ASCII value : "<<endl;
		cout<<endl;
	for(char ch='a';ch<='z';ch++){
		cout<<ch<<" : "<<int(ch)<<endl;
	}
}
